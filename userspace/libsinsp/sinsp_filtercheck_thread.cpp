int32_t sinsp_filter_check_thread::extract_arg(std::string_view fldname,
                                               std::string_view val,
                                               const ppm_param_info** parinfo) {
	std::string::size_type parsed_len = 0;

	//
	// 'arg' and 'resarg' are handled in a custom way
	//
	if(m_field_id == TYPE_APID || m_field_id == TYPE_ANAME || m_field_id == TYPE_AEXE ||
	   m_field_id == TYPE_AEXEPATH || m_field_id == TYPE_ACMDLINE) {
		if(val.size() > fldname.size() && val.at(fldname.size()) == '[') {
			parsed_len = val.find(']');
			if(parsed_len == std::string::npos) {
				throw sinsp_exception("the field '" + string(fldname) +
				                      "' requires an argument but ']' is not found");
			}
			string numstr(val.substr(fldname.size() + 1, parsed_len - fldname.size() - 1));
			m_argid = sinsp_numparser::parsed32(numstr);
			parsed_len++;
		} else {
			throw sinsp_exception("filter syntax error: " + string(val));
		}
	} else if(m_field_id == TYPE_ENV || m_field_id == TYPE_AENV) {
		if(val.size() > fldname.size() && val.at(fldname.size()) == '[') {
			std::string::size_type startpos = fldname.size();
			parsed_len = val.find(']', startpos);

			if(parsed_len == std::string::npos) {
				throw sinsp_exception("the field '" + string(fldname) +
				                      "' requires an argument but ']' is not found");
			}
			m_argname = val.substr(startpos + 1, parsed_len - startpos - 1);
			if(!m_argname.empty() && std::all_of(m_argname.begin(),
			                                     m_argname.end(),
			                                     [](unsigned char c) { return std::isdigit(c); })) {
				m_argid = sinsp_numparser::parsed32(m_argname);
				m_argname.clear();
			}
			parsed_len++;
		} else {
			throw sinsp_exception("filter syntax error: " + string(val));
		}
	} else if(m_field_id == TYPE_CGROUP) {
		if(val.size() > fldname.size() && val.at(fldname.size()) == '.') {
			std::string::size_type endpos;
			for(endpos = fldname.size() + 1; endpos < val.length(); ++endpos) {
				if(!isalpha(val.at(endpos)) && val.at(endpos) != '_') {
					break;
				}
			}

			parsed_len = endpos;
			m_argname = val.substr(fldname.size() + 1, endpos - fldname.size() - 1);
		} else {
			throw sinsp_exception("filter syntax error: " + string(val));
		}
	}

	return (int32_t)parsed_len;
}

int32_t sinsp_filter_check_thread::parse_field_name(std::string_view val,
                                                    bool alloc_state,
                                                    bool needed_for_filtering) {
	if(STR_MATCH("arg")) {
		//
		// 'arg' is handled in a custom way
		//
		throw sinsp_exception("filter error: proc.arg filter not implemented yet");
	} else if(STR_MATCH("proc.apid")) {
		m_field_id = TYPE_APID;
		m_field = &m_info->m_fields[m_field_id];

		int32_t res = 0;

		try {
			res = extract_arg("proc.apid", val, NULL);
		} catch(...) {
			if(val == "proc.apid") {
				m_argid = -1;
				res = (int32_t)val.size();
			}
		}

		return res;
	} else if(STR_MATCH("proc.aname")) {
		m_field_id = TYPE_ANAME;
		m_field = &m_info->m_fields[m_field_id];

		int32_t res = 0;

		try {
			res = extract_arg("proc.aname", val, NULL);
		} catch(...) {
			if(val == "proc.aname") {
				m_argid = -1;
				res = (int32_t)val.size();
			}
		}

		return res;
	} else if(STR_MATCH("proc.aexepath")) {
		m_field_id = TYPE_AEXEPATH;
		m_field = &m_info->m_fields[m_field_id];

		int32_t res = 0;

		try {
			res = extract_arg("proc.aexepath", val, NULL);
		} catch(...) {
			if(val == "proc.aexepath") {
				m_argid = -1;
				res = (int32_t)val.size();
			}
		}

		return res;
	}
	/* note: because of str similarity of proc.aexe to proc.aexepath, this needs to be placed after
	   proc.aexepath */
	else if(STR_MATCH("proc.aexe")) {
		m_field_id = TYPE_AEXE;
		m_field = &m_info->m_fields[m_field_id];

		int32_t res = 0;

		try {
			res = extract_arg("proc.aexe", val, NULL);
		} catch(...) {
			if(val == "proc.aexe") {
				m_argid = -1;
				res = (int32_t)val.size();
			}
		}

		return res;
	} else if(STR_MATCH("proc.acmdline")) {
		m_field_id = TYPE_ACMDLINE;
		m_field = &m_info->m_fields[m_field_id];

		int32_t res = 0;

		try {
			res = extract_arg("proc.acmdline", val, NULL);
		} catch(...) {
			if(val == "proc.acmdline") {
				m_argid = -1;
				res = (int32_t)val.size();
			}
		}

		return res;
	} else if(STR_MATCH("proc.env")) {
		m_field_id = TYPE_ENV;
		m_field = &m_info->m_fields[m_field_id];

		int32_t res = 0;

		try {
			res = extract_arg("proc.env", val, NULL);
		} catch(...) {
			if(val == "proc.env") {
				m_argname.clear();
				res = (int32_t)val.size();
			}
		}

		return res;
	} else if(STR_MATCH("proc.aenv")) {
		m_field_id = TYPE_AENV;
		m_field = &m_info->m_fields[m_field_id];

		int32_t res = 0;

		try {
			res = extract_arg("proc.aenv", val, NULL);
		} catch(...) {
			if(val == "proc.aenv") {
				m_argname.clear();
				m_argid = -1;
				res = (int32_t)val.size();
			}
		}

		return res;
	} else if(STR_MATCH("thread.totexectime")) {
		//
		// Allocate thread storage for the value
		//
		if(alloc_state) {
			auto acc = m_inspector->m_thread_manager->dynamic_fields()->add_field<uint64_t>(
			        "_tmp_sinsp_filter_thread_totexectime");
			m_thread_dyn_field_accessor =
			        std::make_unique<libsinsp::state::dynamic_struct::field_accessor<uint64_t>>(
			                acc.new_accessor<uint64_t>());
		}

		return sinsp_filter_check::parse_field_name(val, alloc_state, needed_for_filtering);
	} else if(STR_MATCH("thread.cgroup") && !STR_MATCH("thread.cgroups")) {
		m_field_id = TYPE_CGROUP;
		m_field = &m_info->m_fields[m_field_id];

		return extract_arg("thread.cgroup", val, NULL);
	} else if(STR_MATCH("thread.cpu")) {
		if(alloc_state) {
			auto acc = m_inspector->m_thread_manager->dynamic_fields()->add_field<uint64_t>(
			        "_tmp_sinsp_filter_thread_cpu");
			m_thread_dyn_field_accessor =
			        std::make_unique<libsinsp::state::dynamic_struct::field_accessor<uint64_t>>(
			                acc.new_accessor<uint64_t>());
		}

		return sinsp_filter_check::parse_field_name(val, alloc_state, needed_for_filtering);
	} else {
		return sinsp_filter_check::parse_field_name(val, alloc_state, needed_for_filtering);
	}
}

uint64_t sinsp_filter_check_thread::extract_exectime(sinsp_evt* evt) {
	uint64_t res = 0;

	if(m_last_proc_switch_times.size() == 0) {
		//
		// Initialize the vector of CPU times
		//
		const scap_machine_info* minfo = m_inspector->get_machine_info();
		ASSERT(minfo->num_cpus != 0);

		if(minfo == NULL || minfo->num_cpus == 0) {
			return res;
		}

		for(uint32_t j = 0; j < minfo->num_cpus; j++) {
			m_last_proc_switch_times.push_back(0);
		}
	}

	uint32_t cpuid = evt->get_cpuid();
	uint64_t ts = evt->get_ts();
	uint64_t lasttime = m_last_proc_switch_times[cpuid];

	if(lasttime != 0) {
		res = ts - lasttime;
	}

	ASSERT(cpuid < m_last_proc_switch_times.size());

	m_last_proc_switch_times[cpuid] = ts;

	return res;
}

uint8_t* sinsp_filter_check_thread::extract_thread_cpu(sinsp_evt* evt,
                                                       uint32_t* len,
                                                       sinsp_threadinfo* tinfo,
                                                       bool extract_user,
                                                       bool extract_system) {
	uint16_t etype = evt->get_type();

	if(etype == PPME_PROCINFO_E) {
		uint64_t user = 0;
		uint64_t system = 0;
		uint64_t tcpu;

		if(extract_user) {
			user = evt->get_param(0)->as<uint64_t>();
		}

		if(extract_system) {
			system = evt->get_param(1)->as<uint64_t>();
		}

		tcpu = user + system;

		uint64_t last_t_tot_cpu = 0;
		tinfo->get_dynamic_field(*m_thread_dyn_field_accessor, last_t_tot_cpu);
		if(last_t_tot_cpu != 0) {
			uint64_t deltaval = tcpu - last_t_tot_cpu;
			m_val.d = (double)deltaval;  // / (ONE_SECOND_IN_NS / 100);
			if(m_val.d > 100) {
				m_val.d = 100;
			}
		} else {
			m_val.d = 0;
		}

		tinfo->set_dynamic_field(*m_thread_dyn_field_accessor, tcpu);

		RETURN_EXTRACT_VAR(m_val.d);
	}

	return NULL;
}

// Some syscall sources, such as the gVisor integration, cannot match events to host PIDs and TIDs.
// The event will retain the PID field which is consistent with the rest of sinsp logic, but it
// won't represent a real PID and so it should not be displayed to the user.
inline bool should_extract_xid(int64_t xid) {
	return xid >= -1 && xid <= UINT32_MAX;
}

uint8_t* sinsp_filter_check_thread::extract_single(sinsp_evt* evt,
                                                   uint32_t* len,
                                                   bool sanitize_strings) {
	*len = 0;
	sinsp_threadinfo* tinfo = evt->get_thread_info();

	if(tinfo == NULL && m_field_id != TYPE_TID && m_field_id != TYPE_EXECTIME &&
	   m_field_id != TYPE_TOTEXECTIME) {
		return NULL;
	}

	switch(m_field_id) {
	case TYPE_TID:
		m_val.s64 = evt->get_tid();
		if(!should_extract_xid(m_val.s64)) {
			return NULL;
		}
		RETURN_EXTRACT_VAR(m_val.s64);
	case TYPE_PID:
		if(!should_extract_xid(tinfo->m_pid)) {
			return NULL;
		}
		RETURN_EXTRACT_VAR(tinfo->m_pid);
	case TYPE_SID:
		RETURN_EXTRACT_VAR(tinfo->m_sid);
	case TYPE_VPGID:
		RETURN_EXTRACT_VAR(tinfo->m_vpgid);
	case TYPE_SID_NAME:
		m_tstr = tinfo->get_ancestor_field_as_string(
		        [](sinsp_threadinfo* t) { return t->m_sid; },
		        [](sinsp_threadinfo* t) { return t->get_comm(); },
		        true);
		RETURN_EXTRACT_STRING(m_tstr);
	case TYPE_SID_EXE:
		m_tstr = tinfo->get_ancestor_field_as_string(
		        [](sinsp_threadinfo* t) { return t->m_sid; },
		        [](sinsp_threadinfo* t) { return t->get_exe(); },
		        true);
		RETURN_EXTRACT_STRING(m_tstr);
	case TYPE_SID_EXEPATH:
		m_tstr = tinfo->get_ancestor_field_as_string(
		        [](sinsp_threadinfo* t) { return t->m_sid; },
		        [](sinsp_threadinfo* t) { return t->get_exepath(); },
		        true);
		RETURN_EXTRACT_STRING(m_tstr);
	case TYPE_VPGID_NAME:
		m_tstr = tinfo->get_ancestor_field_as_string(
		        [](sinsp_threadinfo* t) { return t->m_vpgid; },
		        [](sinsp_threadinfo* t) { return t->get_comm(); },
		        true);
		RETURN_EXTRACT_STRING(m_tstr);
	case TYPE_VPGID_EXE:
		m_tstr = tinfo->get_ancestor_field_as_string(
		        [](sinsp_threadinfo* t) { return t->m_vpgid; },
		        [](sinsp_threadinfo* t) { return t->get_exe(); },
		        true);
		RETURN_EXTRACT_STRING(m_tstr);
	case TYPE_VPGID_EXEPATH:
		m_tstr = tinfo->get_ancestor_field_as_string(
		        [](sinsp_threadinfo* t) { return t->m_vpgid; },
		        [](sinsp_threadinfo* t) { return t->get_exepath(); },
		        true);
		RETURN_EXTRACT_STRING(m_tstr);
	case TYPE_TTY:
		RETURN_EXTRACT_VAR(tinfo->m_tty);
	case TYPE_NAME:
		m_tstr = tinfo->get_comm();
		RETURN_EXTRACT_STRING(m_tstr);
	case TYPE_EXE:
		m_tstr = tinfo->get_exe();
		RETURN_EXTRACT_STRING(m_tstr);
	case TYPE_EXEPATH:
		m_tstr = tinfo->get_exepath();
		RETURN_EXTRACT_STRING(m_tstr);
	case TYPE_ARGS: {
		m_tstr.clear();

		uint32_t j;
		uint32_t nargs = (uint32_t)tinfo->m_args.size();

		for(j = 0; j < nargs; j++) {
			m_tstr += tinfo->m_args[j];
			if(j < nargs - 1) {
				m_tstr += ' ';
			}
		}

		RETURN_EXTRACT_STRING(m_tstr);
	}
	case TYPE_ENV: {
		m_tstr.clear();

		// proc.env[ENV_NAME] use case: returns matched env variable value
		if(!m_argname.empty()) {
			m_tstr = tinfo->get_env(m_argname);
			RETURN_EXTRACT_STRING(m_tstr);
		} else {
			m_tstr = tinfo->concatenate_all_env();
			RETURN_EXTRACT_STRING(m_tstr);
		}
	}
	case TYPE_AENV: {
		m_tstr.clear();

		// in case of proc.aenv without [ENV_NAME] return proc.env; same applies for proc.aenv[0]
		if(m_argname.empty() && m_argid < 1) {
			m_tstr = tinfo->concatenate_all_env();
			RETURN_EXTRACT_STRING(m_tstr);
		}

		// get current tinfo / init for subsequent parent lineage traversal
		sinsp_threadinfo* mt = NULL;
		if(tinfo->is_main_thread()) {
			mt = tinfo;
		} else {
			mt = tinfo->get_main_thread();
			if(mt == NULL) {
				RETURN_EXTRACT_STRING(m_tstr);
			}
		}

		if(!m_argname.empty())  // extract a specific ENV_NAME value
		{
			// start parent lineage traversal
			for(int32_t j = 0; j < 20; j++)  // up to 20 levels, but realistically we will exit way
			                                 // before given the mt nullptr check
			{
				mt = mt->get_parent_thread();

				if(mt == NULL) {
					break;
				}

				m_tstr = mt->get_env(m_argname);
				if(!m_tstr.empty()) {
					break;
				}
			}
			RETURN_EXTRACT_STRING(m_tstr);
		} else if(m_argid > 0) {
			// start parent lineage traversal
			for(int32_t j = 0; j < m_argid; j++) {
				mt = mt->get_parent_thread();

				if(mt == NULL) {
					return NULL;
				}
			}

			// parent tinfo specified found; extract env
			m_tstr = mt->concatenate_all_env();
			RETURN_EXTRACT_STRING(m_tstr);
		}
		RETURN_EXTRACT_STRING(m_tstr);
	}
	case TYPE_CMDLINE: {
		sinsp_threadinfo::populate_cmdline(m_tstr, tinfo);
		RETURN_EXTRACT_STRING(m_tstr);
	}
	case TYPE_EXELINE: {
		m_tstr = tinfo->get_exe() + " ";

		uint32_t j;
		uint32_t nargs = (uint32_t)tinfo->m_args.size();

		for(j = 0; j < nargs; j++) {
			m_tstr += tinfo->m_args[j];
			if(j < nargs - 1) {
				m_tstr += ' ';
			}
		}

		RETURN_EXTRACT_STRING(m_tstr);
	}
	case TYPE_CWD:
		m_tstr = tinfo->get_cwd();
		RETURN_EXTRACT_STRING(m_tstr);
	case TYPE_NTHREADS: {
		m_val.u64 = tinfo->get_num_threads();
		RETURN_EXTRACT_VAR(m_val.u64);
	} break;
	case TYPE_NCHILDS: {
		m_val.u64 = tinfo->get_num_not_leader_threads();
		RETURN_EXTRACT_VAR(m_val.u64);
	} break;
	case TYPE_ISMAINTHREAD:
		m_val.u32 = (uint32_t)tinfo->is_main_thread();
		RETURN_EXTRACT_VAR(m_val.u32);
	case TYPE_EXECTIME: {
		m_val.u64 = 0;
		uint16_t etype = evt->get_type();

		if(etype == PPME_SCHEDSWITCH_1_E || etype == PPME_SCHEDSWITCH_6_E) {
			m_val.u64 = extract_exectime(evt);
		}

		RETURN_EXTRACT_VAR(m_val.u64);
	}
	case TYPE_TOTEXECTIME: {
		m_val.u64 = 0;
		uint16_t etype = evt->get_type();

		if(etype == PPME_SCHEDSWITCH_1_E || etype == PPME_SCHEDSWITCH_6_E) {
			m_val.u64 = extract_exectime(evt);
		}

		sinsp_threadinfo* tinfo = evt->get_thread_info(false);

		if(tinfo != NULL) {
			uint64_t ptot = 0;
			tinfo->get_dynamic_field(*m_thread_dyn_field_accessor, ptot);
			m_val.u64 += ptot;
			tinfo->set_dynamic_field(*m_thread_dyn_field_accessor, m_val.u64);
			RETURN_EXTRACT_VAR(m_val.u64);
		} else {
			return NULL;
		}
	}
	case TYPE_PPID: {
		sinsp_threadinfo* mt = tinfo->get_ancestor_process();
		if(!mt) {
			return NULL;
		}

		if(!should_extract_xid(mt->m_pid)) {
			return NULL;
		}
		RETURN_EXTRACT_VAR(mt->m_pid);
	}
	case TYPE_PNAME: {
		sinsp_threadinfo* ptinfo = tinfo->get_ancestor_process();
		if(!ptinfo) {
			return NULL;
		}

		m_tstr = ptinfo->get_comm();
		RETURN_EXTRACT_STRING(m_tstr);
	}
	case TYPE_PCMDLINE: {
		sinsp_threadinfo* ptinfo = tinfo->get_ancestor_process();
		if(!ptinfo) {
			return NULL;
		}

		sinsp_threadinfo::populate_cmdline(m_tstr, ptinfo);
		RETURN_EXTRACT_STRING(m_tstr);
	}
	case TYPE_ACMDLINE: {
		sinsp_threadinfo* mt = tinfo->get_ancestor_process(m_argid);
		if(!mt) {
			return NULL;
		}

		sinsp_threadinfo::populate_cmdline(m_tstr, mt);
		RETURN_EXTRACT_STRING(m_tstr);
	}
	case TYPE_APID: {
		sinsp_threadinfo* mt = tinfo->get_ancestor_process(m_argid);
		if(!mt) {
			return NULL;
		}

		if(!should_extract_xid(mt->m_pid)) {
			return NULL;
		}
		RETURN_EXTRACT_VAR(mt->m_pid);
	}
	case TYPE_ANAME: {
		sinsp_threadinfo* mt = tinfo->get_ancestor_process(m_argid);
		if(!mt) {
			return NULL;
		}

		m_tstr = mt->get_comm();
		RETURN_EXTRACT_STRING(m_tstr);
	}
	case TYPE_PEXE: {
		sinsp_threadinfo* ptinfo = tinfo->get_ancestor_process();
		if(!ptinfo) {
			return NULL;
		}

		m_tstr = ptinfo->get_exe();
		RETURN_EXTRACT_STRING(m_tstr);
	}
	case TYPE_AEXE: {
		sinsp_threadinfo* mt = tinfo->get_ancestor_process(m_argid);
		if(!mt) {
			return NULL;
		}

		m_tstr = mt->get_exe();
		RETURN_EXTRACT_STRING(m_tstr);
	}
	case TYPE_PEXEPATH: {
		sinsp_threadinfo* ptinfo = tinfo->get_ancestor_process();
		if(!ptinfo) {
			return NULL;
		}

		m_tstr = ptinfo->get_exepath();
		RETURN_EXTRACT_STRING(m_tstr);
	}
	case TYPE_AEXEPATH: {
		sinsp_threadinfo* mt = tinfo->get_ancestor_process(m_argid);
		if(!mt) {
			return NULL;
		}

		m_tstr = mt->get_exepath();
		RETURN_EXTRACT_STRING(m_tstr);
	}
	case TYPE_LOGINSHELLID: {
		sinsp_threadinfo* mt = NULL;
		int64_t* res = NULL;

		if(tinfo->is_main_thread()) {
			mt = tinfo;
		} else {
			mt = tinfo->get_main_thread();

			if(mt == NULL) {
				return NULL;
			}
		}

		sinsp_threadinfo::visitor_func_t check_thread_for_shell = [&res](sinsp_threadinfo* pt) {
			size_t len = pt->m_comm.size();

			if(len >= 2 && pt->m_comm[len - 2] == 's' && pt->m_comm[len - 1] == 'h') {
				res = &pt->m_pid;
			}

			return true;
		};

		// First call the visitor on the main thread.
		check_thread_for_shell(mt);

		// Then check all its parents to see if they are shells
		mt->traverse_parent_state(check_thread_for_shell);

		RETURN_EXTRACT_PTR(res);
	}
	case TYPE_DURATION: {
		if(tinfo->m_clone_ts != 0) {
			m_val.s64 = evt->get_ts() - tinfo->m_clone_ts;
			ASSERT(m_val.s64 > 0);
			RETURN_EXTRACT_VAR(m_val.s64);
		} else {
			return NULL;
		}
	}
	case TYPE_PPID_DURATION: {
		sinsp_threadinfo* ptinfo = tinfo->get_ancestor_process();
		if(!ptinfo) {
			return NULL;
		}

		if(ptinfo->m_clone_ts != 0) {
			m_val.s64 = evt->get_ts() - ptinfo->m_clone_ts;
			ASSERT(m_val.s64 > 0);
			RETURN_EXTRACT_VAR(m_val.s64);
		}
	}
	case TYPE_FDOPENCOUNT:
		m_val.u64 = tinfo->get_fd_opencount();
		RETURN_EXTRACT_VAR(m_val.u64);
	case TYPE_FDLIMIT:
		m_val.s64 = tinfo->get_fd_limit();
		RETURN_EXTRACT_VAR(m_val.s64);
	case TYPE_FDUSAGE:
		m_val.d = tinfo->get_fd_usage_pct_d();
		RETURN_EXTRACT_VAR(m_val.d);
	case TYPE_VMSIZE:
		m_val.u64 = tinfo->m_vmsize_kb;
		RETURN_EXTRACT_VAR(m_val.u64);
	case TYPE_VMRSS:
		m_val.u64 = tinfo->m_vmrss_kb;
		RETURN_EXTRACT_VAR(m_val.u64);
	case TYPE_VMSWAP:
		m_val.u64 = tinfo->m_vmswap_kb;
		RETURN_EXTRACT_VAR(m_val.u64);
	case TYPE_THREAD_VMSIZE:
		if(tinfo->is_main_thread()) {
			m_val.u64 = tinfo->m_vmsize_kb;
		} else {
			m_val.u64 = 0;
		}

		RETURN_EXTRACT_VAR(m_val.u64);
	case TYPE_THREAD_VMRSS:
		if(tinfo->is_main_thread()) {
			m_val.u64 = tinfo->m_vmrss_kb;
		} else {
			m_val.u64 = 0;
		}

		RETURN_EXTRACT_VAR(m_val.u64);
	case TYPE_THREAD_VMSIZE_B:
		if(tinfo->is_main_thread()) {
			m_val.u64 = tinfo->m_vmsize_kb * 1024;
		} else {
			m_val.u64 = 0;
		}

		RETURN_EXTRACT_VAR(m_val.u64);
	case TYPE_THREAD_VMRSS_B:
		if(tinfo->is_main_thread()) {
			m_val.u64 = tinfo->m_vmrss_kb * 1024;
		} else {
			m_val.u64 = 0;
		}

		RETURN_EXTRACT_VAR(m_val.u64);
	case TYPE_PFMAJOR:
		m_val.u64 = tinfo->m_pfmajor;
		RETURN_EXTRACT_VAR(m_val.u64);
	case TYPE_PFMINOR:
		m_val.u64 = tinfo->m_pfminor;
		RETURN_EXTRACT_VAR(m_val.u64);
	case TYPE_CGROUPS: {
		m_tstr.clear();
		auto cgroups = tinfo->cgroups();

		uint32_t j;
		uint32_t nargs = (uint32_t)cgroups.size();

		if(nargs == 0) {
			return NULL;
		}

		for(j = 0; j < nargs; j++) {
			m_tstr += cgroups[j].first;
			m_tstr += "=";
			m_tstr += cgroups[j].second;
			if(j < nargs - 1) {
				m_tstr += ' ';
			}
		}

		RETURN_EXTRACT_STRING(m_tstr);
	}
	case TYPE_CGROUP:
		if(tinfo->get_cgroup(m_argname, m_tstr)) {
			RETURN_EXTRACT_STRING(m_tstr);
		}
		return NULL;
	case TYPE_VTID:
		if(tinfo->m_vtid == -1) {
			return NULL;
		}

		m_val.u64 = tinfo->m_vtid;
		RETURN_EXTRACT_VAR(m_val.u64);
	case TYPE_VPID:
		if(tinfo->m_vpid == -1) {
			return NULL;
		}

		m_val.u64 = tinfo->m_vpid;
		RETURN_EXTRACT_VAR(m_val.u64);
	case TYPE_THREAD_CPU: {
		return extract_thread_cpu(evt, len, tinfo, true, true);
	}
	case TYPE_THREAD_CPU_USER: {
		return extract_thread_cpu(evt, len, tinfo, true, false);
	}
	case TYPE_THREAD_CPU_SYSTEM: {
		return extract_thread_cpu(evt, len, tinfo, false, true);
	}
	case TYPE_NAMETID:
		m_tstr = tinfo->get_comm() + to_string(evt->get_tid());
		RETURN_EXTRACT_STRING(m_tstr);
	case TYPE_IS_EXE_WRITABLE:
		m_val.u32 = tinfo->m_exe_writable;
		RETURN_EXTRACT_VAR(m_val.u32);
	case TYPE_IS_EXE_UPPER_LAYER:
		m_val.u32 = tinfo->m_exe_upper_layer;
		RETURN_EXTRACT_VAR(m_val.u32);
	case TYPE_IS_EXE_LOWER_LAYER:
		m_val.u32 = tinfo->m_exe_lower_layer;
		RETURN_EXTRACT_VAR(m_val.u32);
	case TYPE_IS_EXE_FROM_MEMFD:
		m_val.u32 = tinfo->m_exe_from_memfd;
		RETURN_EXTRACT_VAR(m_val.u32);
	case TYPE_IS_SID_LEADER:
		m_val.u32 = tinfo->m_sid == tinfo->m_vpid;
		RETURN_EXTRACT_VAR(m_val.u32);
	case TYPE_IS_VPGID_LEADER:
		m_val.u32 = tinfo->m_vpgid == tinfo->m_vpid;
		RETURN_EXTRACT_VAR(m_val.u32);
	case TYPE_CAP_PERMITTED:
		m_tstr = sinsp_utils::caps_to_string(tinfo->m_cap_permitted);
		RETURN_EXTRACT_STRING(m_tstr);
	case TYPE_CAP_INHERITABLE:
		m_tstr = sinsp_utils::caps_to_string(tinfo->m_cap_inheritable);
		RETURN_EXTRACT_STRING(m_tstr);
	case TYPE_CAP_EFFECTIVE:
		m_tstr = sinsp_utils::caps_to_string(tinfo->m_cap_effective);
		RETURN_EXTRACT_STRING(m_tstr);
	case TYPE_CMDNARGS: {
		m_val.u64 = (uint32_t)tinfo->m_args.size();
		RETURN_EXTRACT_VAR(m_val.u64);
	}
	case TYPE_CMDLENARGS: {
		m_val.u64 = 0;
		uint32_t j;
		uint32_t nargs = (uint32_t)tinfo->m_args.size();

		for(j = 0; j < nargs; j++) {
			m_val.u64 += tinfo->m_args[j].length();
		}
		RETURN_EXTRACT_VAR(m_val.u64);
	}
	case TYPE_PVPID: {
		sinsp_threadinfo* ptinfo = m_inspector->get_thread_ref(tinfo->m_ptid, false, true).get();

		if(ptinfo != NULL) {
			RETURN_EXTRACT_VAR(ptinfo->m_vpid);
		} else {
			return NULL;
		}
	}
	case TYPE_EXE_INO:
		// Inode 0 is used as a NULL value to indicate that there is no inode.
		if(tinfo->m_exe_ino == 0) {
			return NULL;
		}
		RETURN_EXTRACT_VAR(tinfo->m_exe_ino);
	case TYPE_EXE_INO_CTIME:
		if(tinfo->m_exe_ino_ctime == 0) {
			return NULL;
		}
		RETURN_EXTRACT_VAR(tinfo->m_exe_ino_ctime);
	case TYPE_EXE_INO_MTIME:
		if(tinfo->m_exe_ino_mtime == 0) {
			return NULL;
		}
		RETURN_EXTRACT_VAR(tinfo->m_exe_ino_mtime);
	case TYPE_EXE_INO_CTIME_DURATION_CLONE_TS:
		if(tinfo->m_exe_ino_ctime_duration_clone_ts == 0) {
			return NULL;
		}
		RETURN_EXTRACT_VAR(tinfo->m_exe_ino_ctime_duration_clone_ts);
	case TYPE_EXE_INO_CTIME_DURATION_PIDNS_START:
		if(tinfo->m_exe_ino_ctime_duration_pidns_start == 0) {
			return NULL;
		}
		RETURN_EXTRACT_VAR(tinfo->m_exe_ino_ctime_duration_pidns_start);
	case TYPE_PIDNS_INIT_START_TS:
		if(tinfo->m_pidns_init_start_ts == 0) {
			return NULL;
		}
		RETURN_EXTRACT_VAR(tinfo->m_pidns_init_start_ts);
	case TYPE_PID_CLONE_TS:
		if(tinfo->m_clone_ts == 0) {
			return NULL;
		}
		RETURN_EXTRACT_VAR(tinfo->m_clone_ts);
	case TYPE_PPID_CLONE_TS: {
		sinsp_threadinfo* ptinfo = m_inspector->get_thread_ref(tinfo->m_ptid, false, true).get();

		if(ptinfo != NULL) {
			RETURN_EXTRACT_VAR(ptinfo->m_clone_ts);
		} else {
			return NULL;
		}
	}
	case TYPE_FD_STDIN_TYPE:
	case TYPE_FD_STDOUT_TYPE:
	case TYPE_FD_STDERR_TYPE: {
		int64_t fd = -1;
		if(m_field_id == TYPE_FD_STDIN_TYPE) {
			fd = 0;
		} else if(m_field_id == TYPE_FD_STDOUT_TYPE) {
			fd = 1;
		} else if(m_field_id == TYPE_FD_STDERR_TYPE) {
			fd = 2;
		}
		auto fdtable_ptr = tinfo->get_fd_table();
		if(fdtable_ptr == nullptr) {
			return NULL;
		}
		auto fdinfo = fdtable_ptr->find(fd);
		if(fdinfo == nullptr) {
			return NULL;
		}
		m_tstr = fdinfo->get_typestring();
		RETURN_EXTRACT_STRING(m_tstr);
	}
	case TYPE_FD_STDIN_NAME:
	case TYPE_FD_STDOUT_NAME:
	case TYPE_FD_STDERR_NAME: {
		int64_t fd = -1;
		if(m_field_id == TYPE_FD_STDIN_NAME) {
			fd = 0;
		} else if(m_field_id == TYPE_FD_STDOUT_NAME) {
			fd = 1;
		} else if(m_field_id == TYPE_FD_STDERR_NAME) {
			fd = 2;
		}
		auto fdtable_ptr = tinfo->get_fd_table();
		if(fdtable_ptr == nullptr) {
			return NULL;
		}
		auto fdinfo = fdtable_ptr->find(fd);
		if(fdinfo == nullptr) {
			return NULL;
		}
		m_tstr = fdinfo->m_name.c_str();
		RETURN_EXTRACT_STRING(m_tstr);
	}
	case TYPE_PGID:
		RETURN_EXTRACT_VAR(tinfo->m_pgid);
	case TYPE_PGID_NAME:
		m_tstr = tinfo->get_ancestor_field_as_string(
		        [](sinsp_threadinfo* t) { return t->m_pgid; },
		        [](sinsp_threadinfo* t) { return t->get_comm(); });
		RETURN_EXTRACT_STRING(m_tstr);
	case TYPE_PGID_EXE:
		m_tstr = tinfo->get_ancestor_field_as_string(
		        [](sinsp_threadinfo* t) { return t->m_pgid; },
		        [](sinsp_threadinfo* t) { return t->get_exe(); });
		RETURN_EXTRACT_STRING(m_tstr);
	case TYPE_PGID_EXEPATH:
		m_tstr = tinfo->get_ancestor_field_as_string(
		        [](sinsp_threadinfo* t) { return t->m_pgid; },
		        [](sinsp_threadinfo* t) { return t->get_exepath(); });
		RETURN_EXTRACT_STRING(m_tstr);
	case TYPE_IS_PGID_LEADER:
		m_val.u32 = tinfo->m_pgid == tinfo->m_pid;
		RETURN_EXTRACT_VAR(m_val.u32);
	default:
		ASSERT(false);
		return NULL;
	}
}

bool sinsp_filter_check_thread::compare_full_apid(sinsp_evt* evt) {
	sinsp_threadinfo* tinfo = evt->get_thread_info();

	if(tinfo == NULL) {
		return false;
	}

	sinsp_threadinfo* mt = NULL;

	if(tinfo->is_main_thread()) {
		mt = tinfo;
	} else {
		mt = tinfo->get_main_thread();

		if(mt == NULL) {
			return false;
		}
	}

	//
	// No id specified, search in all of the ancestors
	//
	bool found = false;
	sinsp_threadinfo::visitor_func_t visitor = [this, &found](sinsp_threadinfo* pt) {
		bool res;

		res = compare_rhs(m_cmpop, PT_PID, &pt->m_pid);

		if(res == true) {
			found = true;

			// Can stop traversing parent state
			return false;
		}

		return true;
	};

	mt->traverse_parent_state(visitor);

	return found;
}

